<!-- JavaScript for enhanced form functionality -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    // Enhanced multi-select for tags
    const tagsSelect = document.getElementById('tags');
    if (tagsSelect) {
        // Add visual feedback for selected tags
        tagsSelect.addEventListener('change', function() {
            const selectedOptions = Array.from(this.selectedOptions);
            const selectedCount = selectedOptions.length;
            
            // Update the placeholder text to show selected count
            const label = this.previousElementSibling;
            if (selectedCount > 0) {
                label.innerHTML = `Tags (${selectedCount} selected)`;
            } else {
                label.innerHTML = 'Tags';
            }
        });
    }
    
    // Auto-hide spinner after page load
    const spinner = document.getElementById('spinner');
    if (spinner) {
        setTimeout(() => {
            spinner.classList.remove('show');
        }, 1000);
    }
});
</script>
